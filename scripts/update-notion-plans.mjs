async function run() {
  for (const page of pages) {
    console.log("Updating: " + page.title);
    try {
      await clearPage(page.id);
      await appendBlocks(page.id, page.blocks);
      await notion.pages.update({
        page_id: page.id,
        properties: {
          Titel: { title: [{ text: { content: page.title } }] },
          Status: { select: { name: page.status } },
        },
      });
      console.log("  Done");
    } catch (e) {
      console.error("  Error: " + e.message);
    }
  }
  console.log("All pages updated");
}

run();
